    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Boton para hacer registro total
        String driver = "com.mysql.cj.jdbc.Driver";
        String URL = "jdbc:mysql://localhost:3306/tienda_db";
        String usuario = "root";
        String password = "pedro";  
    
    try {
        Class.forName(driver);
        Connection dbConnection = DriverManager.getConnection(URL, usuario, password);

        Statement statement = dbConnection.createStatement();

        // Insertar pedido
        int idCliente = Integer.parseInt(jTextField1.getText());
        String sqlInsertPedido = "INSERT INTO pedido (fecha, id_cliente) VALUES (CURDATE(), " + idCliente + ")";
        statement.executeUpdate(sqlInsertPedido, Statement.RETURN_GENERATED_KEYS);

        //Obtener id
        int idPedido = 0;
        var rs = statement.getGeneratedKeys();
        if (rs.next()) {
            idPedido = rs.getInt(1);
        }

        //Insertar y actualizar
        for (int i = 0; i < idProductos.size(); i++) {
            int idProducto = idProductos.get(i);
            int cantidad = cantidadProductos.get(i);
            int stock = 0;
            
            //Recoger stock

            Statement st1 = dbConnection.createStatement();
            ResultSet rs2 = st1.executeQuery("SELECT stock FROM producto WHERE id_producto = " + idProducto);
            while (rs2.next()) {
                stock = rs2.getInt(("stock"));
            }
            
            // Insertar en pedido_producto
            String sqlInsertProducto = "INSERT INTO pedido_producto (id_pedido, id_producto, cantidad) " +
                                       "VALUES (" + idPedido + ", " + idProducto + ", " + cantidad + ")";
            statement.executeUpdate(sqlInsertProducto);

            // Actualizar stock
            
            int nuevoStock = stock - cantidad;
            String sqlActualizarStock = "UPDATE producto SET stock = " + nuevoStock +
                                        " WHERE id_producto = " + idProducto;
            statement.executeUpdate(sqlActualizarStock);
        }

        JOptionPane.showMessageDialog(null, "Pedido registrado con Ã©xito. ID: " + idPedido);

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al registrar el pedido");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

